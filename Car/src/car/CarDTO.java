package car;

public class CarDTO {
	private String c_manufacture; // 회사
	private String c_name; // 이름
	private String c_color; // 색상
	private String c_price; // 가격
	private String c_releaseDate; // 출고날짜
	private String c_information; // 상세사양
	private String c_image; // 이미지
	
	public String getC_manufacture() {
		return c_manufacture;
	}
	
	public void setC_manufacture(String c_manufacture) {
		this.c_manufacture = c_manufacture;
	}
	
	public String getC_name() {
		return c_name;
	}
	
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	
	public String getC_color() {
		return c_color;
	}
	
	public void setC_color(String c_color) {
		this.c_color = c_color;
	}
	
	public String getC_price() {
		return c_price;
	}
	
	public void setC_price(String c_price) {
		this.c_price = c_price;
	}
	
	public String getC_releaseDate() {
		return c_releaseDate;
	}
	
	public void setC_releaseDate(String c_releaseDate) {
		this.c_releaseDate = c_releaseDate;
	}
	
	public String getC_information() {
		return c_information;
	}
	
	public void setC_information(String c_information) {
		this.c_information = c_information;
	}
	
	public String getC_image() {
		return c_image;
	}
	
	public void setC_image(String c_image) {
		this.c_image = c_image;
	}
	
}
